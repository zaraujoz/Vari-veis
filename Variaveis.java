public class Variaveis{
    public static void main (String []args){
        byte idade = 20;
        int habitantes = 22000;
        double salário = 2000.0;
        String nome = "Matheus Araujo";
        boolean semaforoAberto = true;
        long celulares = 100000L;
        char letra = 'M'; 
        System.out.println ("Minha idade é: " + idade);
        System.out.println ("Número de habitantes é: " + habitantes);
        System.out.println ("Meu salário é: " + salário);
        System.out.println ("Meu nome é: " + nome);
        System.out.println ("O semáforo está: " + semaforoAberto);
        System.out.println ("O número de celulares é: " + celulares);
        System.out.println ("A letra é: " + letra);
    }

}    